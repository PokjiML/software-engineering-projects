public class Child {
    private String name;
    private int age;
    private String location;
    private String preference;

    public Child(String name, int age, String location) {
        this.name = name;
        this.age = age;
        this.location = location;
        this.preference = preference;
    }

    public String getName() {
        return name;
    }
    public int getAge() {
        return age;
    }
    public String getLocation() {
        return location;
    }
    public String getPreference() {
        return preference;
    }
}